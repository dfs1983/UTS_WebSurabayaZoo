                        <h6>1916 - 1917</h6>
                        <p>
                            Kebun Binatang Surabaya, didirikan oleh seorang jurnalis yang memiliki hobi mengumpulkan
                            binatang yakni,
                            H.F.K. Kommer pada 31 Agustus 1916, dengan nama vereeninging "Soerabaiasche Planten-en
                            Dierentuin" yang
                            berlokasi di Kaliondo. Kemudian pada tanggal 28 september 1917 berpindah lokasi ke jalan
                            Groedo.
                        </p>
                        <h6>1918 - 1920</h6>
                        <p>
                            Tahun 1918 Kebun Binatang Surabaya pertama kali dibuka untuk umum dan mulai ditetapkan tarif
                            masuk (ticket).
                            Namun pada tahun 1920, Kebun Binatang Surabaya kembali pindah lokasi di Jalan Darmo atas
                            jasa Oost-Java
                            Stoomtram (OJS) atau maskapai kereta api yang telah memberikan lokasi seluas 32.000m2 .
                        </p>
                        <h6>2012 - Sekarang</h6>
                        <p>
                            Berdasarkan Peraturan Daerah Kota Surabaya No.19 Tahun 2012 tentang PD Taman Satwa Kebun
                            Binatang Surabaya,
                            terhitung sejak bulan Juli 2012 pengelolahan Kebun Binatang Surabaya semakin profesional
                            dibawah koordinasi
                            Pemerintah Kota Surabaya.
                        </p>
